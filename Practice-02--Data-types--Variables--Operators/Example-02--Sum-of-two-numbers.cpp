/// Напишете програма, която по въведени две числа
/// изкарва на стандартния изход сбора им.

#include <iostream>

using namespace std;

int main()
{
	// Декларираме две променливи от тип цяло число.
    int num1, num2;

	// Въвеждаме от конзолата двете числа.
    cin >> num1 >> num2;
	
	// Извеждаме сбора на num1 и num2.
	// '\n' е само един символ, затова го пишем в единични кавички.
    cout << num1 + num2 << '\n';

    return 0;
}
