/// Стандартна Hello World програма.

// Библиотека за вход и изход с конзолата.
#include <iostream>

// Именовано пространство със стандартните оператори.
using namespace std;

// Главната функция на програмата. Програмата започва изпълнението си от тук.
int main()
{
    // Извеждаме на стандартния изход (конзолата) низа "Hello World",
    // където '\n' е специален символ за нов ред.
    cout << "Hello World!\n";
    
    // Програмата приключва с код 0 - няма проблем.
    return 0;
}
